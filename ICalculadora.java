public interface ICalculadora {
    /**
     *
     * @param valor1
     * @param valor2
     * @return
     */
    public float sumar(int valor1, int valor2);

    /**
     *
     * @param valor1
     * @param valor2
     * @return
     */
    public float restar(int valor1, int valor2);

    /**
     *
     * @param valor1
     * @param valor2
     * @return
     */
    public float multiplicar(int valor1, int valor2);

    /**
     *
     * @param valor1
     * @param valor2
     * @return
     */
    public float dividir(int valor1, int valor2);
}
